export const isValidWord = (word: string): boolean => {
  return VALID_WORDS.includes(word.toUpperCase());
};

export const getRandomWord = (): string => {
  return VALID_WORDS[Math.floor(Math.random() * VALID_WORDS.length)];
};

export const getDailyWord = (date: string): string => {
  // Simple hash function for consistent daily words
  let hash = 0;
  for (let i = 0; i < date.length; i++) {
    const char = date.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash = hash & hash; // Convert to 32-bit integer
  }
  const index = Math.abs(hash) % VALID_WORDS.length;
  return VALID_WORDS[index];
};

